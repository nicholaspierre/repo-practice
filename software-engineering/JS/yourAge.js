var yourAge = 15;

if (yourAge >= 0) {
    println("Congrats, you were born!");
}

if (yourAge === 0) {
    println("You're just a baby!");
}

if (yourAge < 3) {
    println("You can't even speak yet! How can you code?");
}

if (yourAge === 5) {
    println("Get ready for your first ever day of school!");
}

if (yourAge >= 16) {
    println("You can drive! Maybe! Check your state laws.");
}

if (yourAge >= 18) {
    println("You can vote!");
}

if (yourAge >= 21) {
    println("You're eligible for public office!");
}

if (yourAge >= 35) {
    println("You can run for president now!");
}

if (yourAge >= 65) {
    println("You can retire now!");
}